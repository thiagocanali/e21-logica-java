    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed

        if (!"".equals(txtLogin.getText())
                && !"".equals(txtSenha.getPassword())) {

            if (txtLogin.getText().equals(usuario.getLogin())
                    && txtSenha.getText().equals(usuario.getSenha())) {
                painelCompromissos.setVisible(true);
                painelLogin.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(this, "Usuario ou senha incorretos!", "Campos em branco", JOptionPane.ERROR_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Informe todos os campos!", "Campos em branco", JOptionPane.ERROR_MESSAGE);
        }

        painelCompromissos.setVisible(true);
        painelLogin.setVisible(false);
    }//GEN-LAST:event_btnEntrarActionPerformed

