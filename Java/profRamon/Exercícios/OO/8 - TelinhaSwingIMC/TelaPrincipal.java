    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed

        if (!"".equals(txtNome.getText()) && !"".equals(txtPeso.getText()) && !"".equals(txtAltura.getText())) {

            double altura, peso, imc;
            String textoPanel = tpanelResultado.getText() + txtNome.getText() + " - IMC Ã©: ";

            altura = Double.parseDouble(txtAltura.getText().replace(",", "."));
            peso = Double.parseDouble(txtPeso.getText().replace(",", "."));
            imc = peso / (altura * altura);

            textoPanel += imc + "\n";
            tpanelResultado.setText(textoPanel);
        } else {
            JOptionPane.showMessageDialog(this, "Favor preencher todos os campos!", "Campos em branco", JOptionPane.ERROR_MESSAGE);

        }


    }//GEN-LAST:event_btnCalcularActionPerformed

